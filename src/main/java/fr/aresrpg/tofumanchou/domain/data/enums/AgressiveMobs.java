	private int distance;
	private DofusMobs[] ids;

	private AgressiveMobs(int cases, DofusMobs... ids) {
		this.distance = cases;
		this.ids = ids;
	}

	/**
	 * @return the distance
	 */
	public int getDistanceAgro() {
		return distance;
	}

	/**
	 * @return the ids
	 */
	public DofusMobs[] getIds() {
		return ids;
	}

	public static int getDistanceAgro(int mobtype) {
		for (AgressiveMobs a : values()) {
			for (DofusMobs m : a.getIds()) {
				if (m.getId() == mobtype) return a.getDistanceAgro();
			}
		}
		return 0;
	}

	public static AgressiveMobs byId(int id) {
		for (AgressiveMobs a : values())
			if (a.isValid(id)) return a;
		return null;
	}

	public boolean isValid(int id) {
		for (DofusMobs i : ids)
			if (id == i.getId()) return true;
		return false;
	}

	public static boolean canAgro(int distance, int id) {
		AgressiveMobs mob = byId(id);
		if (mob == null) return false;
		return mob.getDistanceAgro() <= distance;
	}

